    public InserirDialog(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        labelValidacao.setVisible(false);
        locais = GestorLocal.listarLocal();
        comboLocal.removeAllItems();

        if(locais.isEmpty()) {
            btnSalvar.setEnabled(false);
            comboLocal.addItem("Não há nenhum local cadastrado.");
        } else {
            for(String local : locais) {
                comboLocal.addItem(local);
            }
        }
        montaCombosData();
        montaCombosHorario();
    }
    
    public void montaCombosData() {
        comboDia.removeAllItems();
        comboMes.removeAllItems();
        comboAno.removeAllItems();
        
        for(int i=1; i <= 31; i++) {
            if(i < 10) {
                 comboDia.addItem("0"+Integer.toString(i));
            } else {
                comboDia.addItem(Integer.toString(i));
            }
        }
        for(int i=1; i <= 12; i++) {
            if(i < 10) {
                comboMes.addItem("0"+Integer.toString(i));
            } else {
                comboMes.addItem(Integer.toString(i));
            }
        }
        for(int i=1990; i <= 2030; i++) {
            comboAno.addItem(Integer.toString(i));
        }
    }
    
    public void montaCombosHorario() {
        comboHora.removeAllItems();
        comboMinuto.removeAllItems();
        comboSegundo.removeAllItems();
        
        for(int i=0; i <= 23; i++) {
            if(i <= 9) {
                comboHora.addItem("0"+Integer.toString(i));
            } else {
                comboHora.addItem(Integer.toString(i));
            }
        }
        for(int i=0; i <= 59; i++) {
            if(i <= 9) {
                comboMinuto.addItem("0"+Integer.toString(i));
                comboSegundo.addItem("0"+Integer.toString(i));
            } else {
                comboMinuto.addItem(Integer.toString(i));            
                comboSegundo.addItem(Integer.toString(i));
            }
        }
            
    }
    

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       boolean campoIdVazio = campoIdentificacao.getText().isEmpty();

        boolean campoFaseVazio = campoFase.getText().isEmpty();
        if(campoIdVazio || campoFaseVazio) {
            labelValidacao.setVisible(true);
        } else {
            int id = Integer.parseInt(campoIdentificacao.getText());
            
            String dataHora = montaStringDataHora();
            
            int numeroDeFase = Integer.parseInt(campoFase.getText());
            String local = (String)comboLocal.getSelectedItem();
            String observacoes = campoObservacoes.getText();
            
            Competicao c = new Competicao(id, dataHora, numeroDeFase, observacoes, local);
            GestorCompeticao.inserirCompeticao(c);
            
            dispose();            
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    public String montaStringDataHora() {
            String dia = (String)comboDia.getSelectedItem();
            String mes = (String)comboMes.getSelectedItem();
            String ano = (String)comboAno.getSelectedItem();
            String diaEHora = dia+"/"+mes+"/"+ano+" ";
            
            String hora = (String)comboHora.getSelectedItem();
            String minuto = (String)comboMinuto.getSelectedItem();
            String segundo = (String)comboSegundo.getSelectedItem();
            diaEHora = diaEHora + hora + ":" + minuto + ":" + segundo;
            return diaEHora;
    }
    

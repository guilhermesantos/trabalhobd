    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        String usuario = txtLogin.getText();
        char[] password = txtSenha.getPassword();
        String senha = new String(password);
        String ip = txtIP.getText();
        String porta = txtPorta.getText();
        String SID = txtSID.getText();
        boolean ret = ConexaoBD.getInstance().conectarBanco(usuario, senha, ip, porta, SID);
        if(ret){
            dispose();
            InterfacePanda gui = new InterfacePanda();
            gui.setVisible(true);
        }else{
            labelErroConexao.setVisible(true);
        }
    }//GEN-LAST:event_btnConectarActionPerformed

